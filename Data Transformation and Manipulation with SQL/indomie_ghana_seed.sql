-- ============================================================
-- TABLE: orders
-- Known data quality issues embedded:
--   * ~5% of quantity_cartons are NULL (missing entries)
--   * ~3% duplicate order rows (same distributor, date, variant)
--   * ~4% quantities entered as individual packs not cartons (e.g. 800 instead of 20)
--   * Some orders placed on Ghana public holidays
--   * Region entries inconsistent (mirrors distributors table)
-- ============================================================
CREATE TABLE orders (
    order_id         SERIAL PRIMARY KEY,
    distributor_id   INT REFERENCES distributors(distributor_id),
    variant_id       INT REFERENCES product_variants(variant_id),
    order_date       DATE NOT NULL,
    quantity_cartons INT,
    region           VARCHAR(100),
    order_status     VARCHAR(30) DEFAULT 'confirmed'
);

