-- ============================================================
-- TABLE: product_variants
-- ============================================================
CREATE TABLE product_variants (
    variant_id       SERIAL PRIMARY KEY,
    variant_name     VARCHAR(100) NOT NULL,
    category         VARCHAR(50),
    unit_price_ghs   DECIMAL(10,2) NOT NULL,
    pack_per_carton  INT DEFAULT 40
